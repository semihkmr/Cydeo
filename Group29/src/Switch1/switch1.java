package Switch1;

public class switch1 {
    public static void main(String[] args) {
       int number =3;

        switch (number){
            case 1:
                System.out.println("monday");
                break;
            case 2:
                System.out.println("tuesday"); // if you do not use the braak code, program will continue to output, whenever you want to program stop you need to put break code!!
                break;
            case 3:

                 System.out.println("Wednesday");
                 break;
            case 4:
                System.out.println("Thursday");
                break;


        }
    }
}
